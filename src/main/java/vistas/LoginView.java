    private void jViewPasswordBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jViewPasswordBtnActionPerformed
        if (passwordVisible) {
            // ocultar contraseña
            jPasswordField.setEchoChar('•');
            jViewPasswordBtn.setIcon(
                    new ImageIcon(getClass().getResource("/icons/cerrar.png"))
            );
            passwordVisible = false;
        } else {
            // mostrar contraseña
            jPasswordField.setEchoChar((char) 0);
            jViewPasswordBtn.setIcon(
                    new ImageIcon(getClass().getResource("/icons/abrir.png"))
            );
            passwordVisible = true;
        }
    }//GEN-LAST:event_jViewPasswordBtnActionPerformed

    private void iniciarProgressBar() {
        jProgressBar.setValue(0);
        jProgressBar.setString("");
        jProgressBar.setStringPainted(true);

        Timer timer = new Timer(20, null);

        timer.addActionListener(new ActionListener() {
            private int value = 0;
            private final int total = 100;

            @Override
            public void actionPerformed(ActionEvent e) {
                value += 1;
                jProgressBar.setValue(value);

                if (value >= total) {
                    ((Timer) e.getSource()).stop();

                    SwingUtilities.invokeLater(() -> {
                        new MenuPrincipalForm(Sesion.rolActivo).setVisible(true);
                        dispose();
                    });
                }
            }
        });

        timer.start();
    }

    private void bloquearBoton() {
        jIniciarBtn.setEnabled(false);
        jUsernameField.setEnabled(false);
        jPasswordField.setEnabled(false);
        jViewPasswordBtn.setEnabled(false);
    }

