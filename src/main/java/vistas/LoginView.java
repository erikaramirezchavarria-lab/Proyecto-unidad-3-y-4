    private void jIniciarBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jIniciarBtnActionPerformed
        String user = jUsernameField.getText().trim();
        String pass = new String(jPasswordField.getPassword());

        if (user.isEmpty() || pass.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Ingresa usuario y contraseña!");
            return;
        }
        
        // 🔥 CORRECCIÓN: Encriptamos la contraseña plana a SHA-256 usando la utilería
        String passEncriptada = Encriptador.encriptarSHA256(pass);
        
        UsuarioDAO dao = new UsuarioDAO();
        // Le pasamos la contraseña encriptada (64 caracteres) para que coincida con la BD corregida
        String rol = dao.validarLogin(user, passEncriptada); 
        
        if (rol != null) {
            Sesion.usuarioActivo = user;
            Sesion.rolActivo = rol;
            intentos = 0;

            JOptionPane.showMessageDialog(this, 
                    "Bienvenido " + user + " (" + rol + ")");

            iniciarProgressBar(); 
            
        } else {
            intentos++;
            JOptionPane.showMessageDialog(this, 
                    "Credenciales incorrectas. Intento " + intentos + "/3");
            
            jUsernameField.setText("");
            jPasswordField.setText("");
            
            if (intentos >= 3) {
                bloquearBoton();
            }
        }
    }//GEN-LAST:event_jIniciarBtnActionPerformed

